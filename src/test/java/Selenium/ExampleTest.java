    driver.manage().window().setSize(new Dimension(1050, 660));
    driver.manage().timeouts().implicitlyWait(5, TimeUnit.SECONDS);
    List<WebElement> reviews = driver.findElements(By.xpath("//div[@class='a-expander-collapsed-height a-row a-expander-container a-expander-partial-collapse-container']"));
    List<String> list = new ArrayList<>();
//    List<WebElement> stars = driver.findElements(By.xpath("//div[@class='a-row']"));
