    for (WebElement webElement : reviews) {
      String review = webElement.getText();
      System.out.println(review);
      list.add(review);
    }
    for (WebElement webElement : stars){
      String star = webElement.getText();
      System.out.println(star);
      starlist.add(star);
    }
    FileWriter writer = new FileWriter("output12.txt");
    for(String str: list) {
      writer.write(str + System.lineSeparator() + System.lineSeparator());
    }
    writer.close();

    FileWriter writer2 = new FileWriter("output13.txt");
    for(String str: starlist) {
      writer2.write(str + System.lineSeparator() + System.lineSeparator());
    }
    writer.close();


